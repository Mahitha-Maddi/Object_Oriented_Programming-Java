public void displayVitalSigns(){
    DefaultTableModel dtm=(DefaultTableModel)VitalSignstbl.getModel();
    dtm.setRowCount(0);
    label:
    for(VitalSigns vs:vsh.getVitalSignsHistory()){
        if((vs.getBloodPressure()<minbp) || (vs.getBloodPressure()>maxbp))
        {Object row[]=new Object[2];
       
        row[0]=vs;
        row[1]=vs.getBloodPressure();
        dtm.addRow(row);}
        continue label;
    }
}
  
