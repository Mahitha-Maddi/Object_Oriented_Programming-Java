    private void ViewDetailsBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ViewDetailsBtnActionPerformed

        int selectedRow=VitalSignstbl.getSelectedRow();
        if(selectedRow>=0){
            VitalSigns vs=(VitalSigns)VitalSignstbl.getValueAt(selectedRow,0);
            bloodPressureTextField.setText(String.valueOf(vs.getBloodPressure()));
            temparatureTextField.setText(String.valueOf(vs.getTemparature()));
            pulseTextField.setText(String.valueOf(vs.getPulse()));
            dateTextField.setText(String.valueOf(vs.getDate()));
        }
        else{
            JOptionPane.showMessageDialog(null,"Please select a row you want to view");
        }
    }//GEN-LAST:event_ViewDetailsBtnActionPerformed

    private void Update1BtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Update1BtnActionPerformed
        int selectedRow=VitalSignstbl.getSelectedRow();
        if(selectedRow>=0){
            setAllEnabled(true);
            VitalSigns vs=(VitalSigns)VitalSignstbl.getValueAt(selectedRow,0);
            bloodPressureTextField.setText(String.valueOf(vs.getBloodPressure() + ""));
            temparatureTextField.setText(String.valueOf(vs.getTemparature()+ ""));
            pulseTextField.setText(String.valueOf(vs.getPulse()+ ""));
            dateTextField.setText(String.valueOf(vs.getDate()));
        }
        else{
            JOptionPane.showMessageDialog(null,"Please select a row you want to update");
        }
    }//GEN-LAST:event_Update1BtnActionPerformed

    private void DeleteBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DeleteBtnActionPerformed
        int selectedRow=VitalSignstbl.getSelectedRow();
        if(selectedRow>=0){
            VitalSigns vs=(VitalSigns)VitalSignstbl.getValueAt(selectedRow,0);
            int dialogButton=0;
            int dialogResult=JOptionPane.showConfirmDialog (null, "Are you sure you want to delete selected row","Warning",dialogButton);

            if (dialogResult == JOptionPane.YES_OPTION) {

                vsh.deleteVitalSign(vs);

                JOptionPane.showMessageDialog(null,"Deleted successfully");
                displayVitalSigns();} }
        else{
            JOptionPane.showMessageDialog(null,"Please select a row");
        }
    }//GEN-LAST:event_DeleteBtnActionPerformed

