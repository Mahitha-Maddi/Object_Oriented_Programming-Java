    private void ConfirmBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ConfirmBtnActionPerformed
            
         int selectedRow=VitalSignstbl.getSelectedRow();
      if(selectedRow>=0){
          VitalSigns vs=(VitalSigns)VitalSignstbl.getValueAt(selectedRow,0);
          double bp=Double.parseDouble(bloodPressureTextField.getText());
       double t=Double.parseDouble(temparatureTextField.getText());
       int p=Integer.parseInt(pulseTextField.getText());
       String d =dateTextField.getText();
       vs.setBloodPressure(bp);
          vs.setPulse(p);
             vs.setTemparature(t);
                vs.setDate(d);
                 displayVitalSigns();
                 setAllEnabled(false);
                  bloodPressureTextField.setText("");
       temparatureTextField.setText("");
       pulseTextField.setText("");
      dateTextField.setText("");
                 
      }
      else{
JOptionPane.showMessageDialog(null,"Please select a row");}
    }//GEN-LAST:event_ConfirmBtnActionPerformed

