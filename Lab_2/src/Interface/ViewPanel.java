    private void DeleteBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DeleteBtnActionPerformed
 int selectedRow=VitalSignstbl.getSelectedRow();
      if(selectedRow>=0){
          VitalSigns vs=(VitalSigns)VitalSignstbl.getValueAt(selectedRow,0);
           int dialogButton=0;
        int dialogResult=JOptionPane.showConfirmDialog (null, "Are you sure you want to delete selected row","Warning",dialogButton);

    if (dialogResult == JOptionPane.YES_OPTION) { 

           vsh.deleteVitalSign(vs);
          
       JOptionPane.showMessageDialog(null,"Deleted successfully");
          displayVitalSigns();} }
      else{
JOptionPane.showMessageDialog(null,"Please select a row");
      }        
    }//GEN-LAST:event_DeleteBtnActionPerformed

